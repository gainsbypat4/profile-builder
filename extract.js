export default async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") {
    return res.status(200).end();
  }

  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { resumeText } = req.body;

  if (!resumeText) {
    return res.status(400).json({ error: "Resume text is required" });
  }

  try {
    const response = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "x-api-key": process.env.ANTHROPIC_API_KEY,
        "anthropic-version": "2023-06-01"
      },
      body: JSON.stringify({
        model: "claude-sonnet-4-20250514",
        max_tokens: 2000,
        messages: [{
          role: "user",
          content: `Extract structured data from this nurse resume. Return ONLY valid JSON, no other text, no markdown code blocks.

Resume:
${resumeText}

Return this exact JSON structure (fill in what you find, use null for missing fields):
{
  "personalInfo": {
    "fullName": "",
    "phone": "",
    "email": "",
    "location": ""
  },
  "licenses": [
    {"state": "", "compact": true, "type": "RN"}
  ],
  "certifications": ["BLS", "ACLS"],
  "education": {
    "degree": "BSN",
    "school": "",
    "graduationDate": ""
  },
  "workHistory": [
    {
      "title": "",
      "facility": "",
      "city": "",
      "state": "",
      "unit": "",
      "startDate": "",
      "endDate": "",
      "responsibilities": [""],
      "chargeExperience": false
    }
  ],
  "yearsExperience": 0,
  "primarySpecialty": ""
}`
        }]
      })
    });

    const data = await response.json();
    
    if (data.error) {
      return res.status(500).json({ error: data.error.message });
    }

    const responseText = data.content[0].text;
    const cleanJson = responseText.replace(/```json\n?/g, "").replace(/```\n?/g, "").trim();
    const parsed = JSON.parse(cleanJson);

    return res.status(200).json(parsed);
  } catch (error) {
    console.error("API Error:", error);
    return res.status(500).json({ error: error.message || "Failed to extract data" });
  }
}
